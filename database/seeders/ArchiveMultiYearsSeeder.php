        return match ($yearId) {
            4  => self::STUDENTS_2023_2024,
            5  => self::STUDENTS_2022_2023,
            6  => self::STUDENTS_2021_2022,
            7  => self::STUDENTS_2020_2021,
            8  => self::STUDENTS_2019_2020,

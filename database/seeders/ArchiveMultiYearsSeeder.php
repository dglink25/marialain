    // ─────────────────────────────────────────────────────────────────────────
    //  run()
    // ─────────────────────────────────────────────────────────────────────────

    public function run(): void
    {
        $this->command->info('══════════════════════════════════════════════════');
        $this->command->info(' Seeder : Rattrapage multi-années 2017-2018 → 2023-2024');
        $this->command->info('══════════════════════════════════════════════════');

        foreach (self::ACADEMIC_YEARS as [$yearId, $yearName, $prefix]) {

            $this->command->info("\n── Traitement de l'année {$yearName} (id={$yearId}) ──");

            // 1. S'assurer que l'année académique existe
            $this->ensureAcademicYear($yearId, $yearName);

            // 2. Créer les classes nécessaires pour cette année
            $students  = $this->getStudentsForYear($yearId);
            $classIdMap = $this->ensureClasses($yearId, $students);

            // 3. Insérer les apprenants
            $this->insertStudents($yearId, $prefix, $students, $classIdMap);
        }

        $this->command->info("\n══════════════════════════════════════════════════");
        $this->command->info(' Seeder multi-années terminé.');
        $this->command->info('══════════════════════════════════════════════════');
    }

    // ─── Étape 1 : Assurer l'existence de l'année académique ─────────────────

    private function ensureAcademicYear(int $id, string $name): void
    {
        $exists = DB::table('academic_years')->where('id', $id)->exists();
        if (! $exists) {
            DB::table('academic_years')->insert([
                'id'         => $id,
                'name'       => $name,
                'active'     => false,
                'created_at' => Carbon::now(),
                'updated_at' => Carbon::now(),
            ]);
            $this->command->info("  ✔ Année \"{$name}\" créée.");
        } else {
            $this->command->line("  · Année \"{$name}\" déjà présente.");
        }
    }

    // ─── Étape 2 : Créer les classes ─────────────────────────────────────────

    private function ensureClasses(int $yearId, array $students): array
    {
        $needed = array_unique(array_column($students, 4));
        $classIdMap = [];
        $now = Carbon::now();

        foreach ($needed as $className) {
            $entityId = self::CLASS_ENTITY[$className] ?? self::ENTITY_SECONDAIRE;
            $fees     = self::CLASS_FEES[$className]   ?? [0, 10000, 5000];

            $existing = DB::table('classes')
                ->where('name',             $className)
                ->where('academic_year_id', $yearId)
                ->where('entity_id',        $entityId)
                ->first();

            if ($existing) {
                $classIdMap[$className] = $existing->id;
                $this->command->line("  · Classe «{$className}» déjà présente (id={$existing->id}).");
            } else {
                $id = DB::table('classes')->insertGetId([
                    'name'                => $className,
                    'entity_id'           => $entityId,
                    'academic_year_id'    => $yearId,
                    'teacher_id'          => null,
                    'school_fees'         => $fees[0],
                    'registration_fee'    => $fees[1],
                    're_registration_fee' => $fees[2],
                    'description'         => null,
                    'created_at'          => $now,
                    'updated_at'          => $now,
                ]);
                $classIdMap[$className] = $id;
                $this->command->info("  ✔ Classe «{$className}» créée (id={$id}).");
            }
        }

        return $classIdMap;
    }

    // ─── Étape 3 : Insérer les apprenants ────────────────────────────────────

    private function insertStudents(
        int $yearId,
        string $prefix,
        array $students,
        array $classIdMap
    ): void {
        $now      = Carbon::now();
        $inserted = 0;
        $skipped  = 0;
        $counter  = 1;

        foreach ($students as [$lastName, $firstName, $gender, $phone, $className]) {

            $classId = $classIdMap[$className] ?? null;
            if (! $classId) {
                $this->command->warn("  ⚠ Classe «{$className}» introuvable — {$lastName} {$firstName} ignoré.");
                $skipped++;
                continue;
            }

            // Détection de doublon : même nom de famille + au moins un token de prénom commun
            if ($this->studentAlreadyExists($lastName, $firstName)) {
                $this->command->line("  · {$lastName} {$firstName} déjà présent (doublon) — sauté.");
                $skipped++;
                continue;
            }

            $numEduc = $this->generateUniqueNumEduc($prefix, $counter);
            $counter++;

            $fees      = self::CLASS_FEES[$className] ?? [0, 10000, 5000];
            $totalFees = $fees[0] + $fees[2]; // school_fees + re_registration_fee

            DB::table('students')->insert([
                'first_name'           => $firstName,
                'last_name'            => $lastName,
                'birth_date'           => '2000-01-01',
                'birth_place'          => null,
                'age'                  => 20,
                'gender'               => $gender,
                'num_educ'             => $numEduc,
                'entity_id'            => self::CLASS_ENTITY[$className] ?? self::ENTITY_SECONDAIRE,
                'academic_year_id'     => $yearId,
                'class_id'             => $classId,
                'birth_certificate'    => null,
                'vaccination_card'     => null,
                'previous_report_card' => null,
                'diploma_certificate'  => null,
                'parent_full_name'     => null,
                'parent_email'         => null,
                'parent_phone'         => $phone,
                'school_fees'          => $fees[0],
                'is_validated'         => true,
                'amount_paid'          => 0,
                'school_fees_paid'     => 0,
                'fully_paid'           => false,
                'registration_type'    => 're_registration',
                'total_fees'           => $totalFees,
                'created_at'           => $now,
                'updated_at'           => $now,
            ]);

            $this->command->line("  ✔ Inséré : {$lastName} {$firstName} → {$className} [{$numEduc}]");
            $inserted++;
        }

        $this->command->info("  ➜ Résultat : {$inserted} insérés, {$skipped} ignorés.");
    }

    // ─── Détection de doublon ─────────────────────────────────────────────────

    /**
     * Retourne true si un apprenant avec le même nom de famille ET au moins
     * un token de prénom commun existe déjà en base (toutes années confondues).
     */
    private function studentAlreadyExists(string $lastName, string $firstName): bool
    {
        $candidates = DB::table('students')
            ->whereRaw('LOWER(last_name) = ?', [mb_strtolower($lastName)])
            ->pluck('first_name');

        if ($candidates->isEmpty()) {
            return false;
        }

        $csvTokens = $this->tokenizeFirstName($firstName);

        foreach ($candidates as $dbFirst) {
            $dbTokens = $this->tokenizeFirstName($dbFirst);
            if (! empty(array_intersect($csvTokens, $dbTokens))) {
                return true;
            }
        }

        return false;
    }

    /**
     * Découpe un prénom composé en tokens normalisés (sans accents, minuscules).
     * Séparateurs : espace, tiret, point. Tokens < 2 chars ignorés.
     */
    private function tokenizeFirstName(string $name): array
    {
        $normalized = mb_strtolower($name);
        $normalized = iconv('UTF-8', 'ASCII//TRANSLIT//IGNORE', $normalized);
        $parts = preg_split('/[\s\-\.]+/', $normalized, -1, PREG_SPLIT_NO_EMPTY);
        return array_values(array_filter($parts, fn($t) => mb_strlen($t) >= 2));
    }

    // ─── Génération num_educ unique ───────────────────────────────────────────

    private function generateUniqueNumEduc(string $prefix, int $counter): string
    {
        do {
            $numEduc = $prefix . '-' . str_pad($counter, 4, '0', STR_PAD_LEFT);
            $counter++;
            $exists = DB::table('students')->where('num_educ', $numEduc)->exists();
        } while ($exists);

        return $numEduc;
    }
